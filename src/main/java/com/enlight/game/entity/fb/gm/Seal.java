public class Seal {
	
	private int id;
	
	private String serverZoneId;
	
	private String gameId;
	
	private String serverId;
	
	private String platForm;
	
	private String guid;
	
	private String name;
	
	private String account;
	
	private String sealTime;
	
	private String sealStart;
	
	private String sealEnd;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getServerZoneId() {
		return serverZoneId;
	}

	public void setServerZoneId(String serverZoneId) {
		this.serverZoneId = serverZoneId;
	}

	public String getGameId() {
		return gameId;
	}

	public void setGameId(String gameId) {
		this.gameId = gameId;
	}

	public String getServerId() {
		return serverId;
	}

	public void setServerId(String serverId) {
		this.serverId = serverId;
	}

	public String getPlatForm() {
		return platForm;
	}

	public void setPlatForm(String platForm) {
		this.platForm = platForm;
	}

	public String getGuid() {
		return guid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getSealTime() {
		return sealTime;
	}

	public void setSealTime(String sealTime) {
		this.sealTime = sealTime;
	}

	public String getSealStart() {
		return sealStart;
	}

	public void setSealStart(String sealStart) {
		this.sealStart = sealStart;
	}

	public String getSealEnd() {
		return sealEnd;
	}

	public void setSealEnd(String sealEnd) {
		this.sealEnd = sealEnd;
	}

}
